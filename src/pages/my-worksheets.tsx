interface Props {
  profileId: string;
}

const WorksheetList: React.FC<Props> = ({ profileId }) => {
  const { data: profiles, refetch: refetchProfiles } =
    api.teacherProfile.getWorksheets.useQuery(
      undefined // no input
    );

  const worksheets = profiles?.at(0)?.worksheets;

  if (worksheets?.length == 0) {
    return <></>;
  } else {
    return <></>;
  }
};

